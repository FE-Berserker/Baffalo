function d=d_Cal(obj)
% 动静态比计算 (Dynamic-to-Static Ratio Calculation)
% Author: Xie Yu
%
% 功能说明：
% 根据邵氏硬度HS，计算橡胶材料的动静态比d(Ed/Es)。
% 动静态比d表示动态弹性模量Ed与静态弹性模量Es的比值。
%
% 计算公式：
%   d = 1e-5 * HS^2 + 0.0181 * HS + 0.5954
%   其中HS为邵氏硬度值
%   1e-5是单位转换系数
%   -0.0181、0.5954是经验系数
%
% 输出：
%   d = 动静态比(Ed/Es)
%
% 物理意义：
%   d值越接近1，表示橡胶材料在动态和静态条件下的弹性特性越接近。
%   通常用于橡胶材料的动态力学分析。

HS=obj.input.HS;              % 获取邵氏硬度输入
d=1e-5*HS^2+0.0181*HS+0.5954;  % 计算动静态比
end
