function G=G_Cal(obj)
% 剪切模量计算 (Shear Modulus Calculation)
% Author: Xie Yu
%
% 功能说明：
% 根据邵氏硬度HS，计算橡胶材料的剪切模量G。
% 剪切模量G与弹性模量E的关系：G = E / (2*(1+v))
%
% 计算公式：
%   G = 5e-4 * HS^2 - 0.0133 * HS + 0.2182
%   其中HS为邵氏硬度值
%   5e-4是单位转换系数
%   -0.0133、-0.2182、0.2182是经验系数
%
% 输出：
%   G = 剪切模量 [N/mm²]
%
% 物理意义：
%   剪切模量G描述材料抵抗剪切变形的能力
%   邵氏硬度越大，橡胶材料越硬，剪切模量越高

HS=obj.input.HS;              % 获取邵氏硬度输入
G=5e-4*HS^2-0.0133*HS+0.2182;  % 计算剪切模量 [N/mm²]
end