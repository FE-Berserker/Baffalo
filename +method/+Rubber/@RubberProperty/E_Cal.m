function E=E_Cal(obj)
% 弹性模量计算 (Young's Modulus Calculation)
% Author: Xie Yu
%
% 功能说明：
% 根据邵氏硬度HS，计算橡胶材料的弹性模量(杨氏模量)E。
% 弹性模量E描述材料抵抗弹性变形的能力。
%
% 计算公式：
%   E = 0.001 * HS^2 + 0.0293 * HS + 0.6909
%   其中HS为邵氏硬度值
%   0.001、0.0293、0.6909是经验拟合系数
%
% 输出：
%   E = 弹性模量 [N/mm²]
%
% 物理意义：
%   弹性模量E越大，材料越硬，抵抗弹性变形的能力越强。
%   与邵氏硬度的关系：邵氏硬度越高，弹性模量越大。
%
% 应用场景：
%   用于有限元分析中的材料属性定义

HS=obj.input.HS;              % 获取邵氏硬度输入
E=0.001*HS^2+0.0293*HS+0.6909;  % 计算弹性模量 [N/mm²]

end
